public class stringconcatenate {
    public static void main(String[] args) {
        String str1 = "Ayush";
        String str2 = "Mishra";
        String str3 = str1+str2;
        System.out.println(str3);
        String str4 = str1.concat(str2);
        System.out.println(str4);
        System.out.println(str4.indexOf("ush"));

    }
    
}
