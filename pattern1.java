import java.util.Scanner;

public class pattern1 {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        System.out.println("Enter the no of rows you want: ");
        int n = s.nextInt();

        for(int i=1;i<=n;i++){
            System.out.print("*"); //work

            System.out.println(); 

        }
    }
    
}
